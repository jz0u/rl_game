/**
 * Armory â€” static catalogue of all equippable items, grouped by slot.
 *
 * Each item follows this schema:
 * @typedef {object} Item
 * @property {string} id              - Unique key used as the Phaser texture key for the shop icon.
 * @property {string} displayName     - Human-readable name shown in the shop UI.
 * @property {string} equipSlot       - Equipment slot: 'head' | 'amulet' | 'shoulder' | 'hands' | 'body_inner' | 'body_outer' | 'legs' | 'feet' | 'primary' | 'secondary'.
 * @property {string} itemType        - 'armor' or 'weapon'.
 * @property {string} [weightClass]   - Armor only: 'light' | 'medium' | 'heavy'.
 * @property {string} [rangeType]     - Weapon only: 'melee' | 'ranged'.
 * @property {string} [handType]      - Primary weapons only: 'one' | 'two'.
 * @property {string} [weaponSubtype] - Weapon only: e.g. 'sword' | 'axe' | 'bow' | 'crossbow' | 'spear' | 'hammer' | 'mace' | 'pike'.
 * @property {string} baseName        - Sprite filename prefix used to build animation asset paths.
 * @property {string} iconPath        - Path to the paperdoll icon (loaded as item.id).
 * @property {object} stats           - Flat key/value stat bonuses (e.g. { armorValue: 2 }).
 * @property {number} value           - Gold cost of the item.
 * @property {string} description     - Flavour text shown in the shop preview.
 */

const SLOT_FOLDER = {
  primary:   'primary_weapon',
  secondary: 'secondary_weapon',
  feet:      'feets',
};
function folderFor(slot) { return SLOT_FOLDER[slot] ?? slot; }
/**
 * Build the iconPath for an item.
 * @param {string} slot - Slot name (maps to folder via SLOT_FOLDER).
 * @param {string} baseName - The unique item baseName (= subdirectory name within slot folder,
 *   also used as the Phaser texture key prefix).
 * @param {string} [fileName] - The actual filename prefix inside the subdirectory when it
 *   differs from baseName (e.g. Warfare weapons use _Main_/_Off_ in folder names but not files).
 */
function iconPath(slot, baseName, fileName) {
  const file = fileName ?? baseName;
  return `assets/armory/${folderFor(slot)}/${baseName}/${file}_Paperdoll2.png`;
}

